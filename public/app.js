// Get current language from localStorage or default to Bangla
let currentLang = localStorage.getItem('language') || 'bn';

// Function to get nested translation
function getTranslation(path) {
    const keys = path.split('.');
    let value = translations[currentLang];

    for (const key of keys) {
        value = value[key];
        if (value === undefined) return path;
    }

    return value;
}

// Function to update page content
function updateContent() {
    const elements = document.querySelectorAll('[data-i18n]');

    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        const translation = getTranslation(key);

        if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
            element.placeholder = translation;
        } else {
            element.innerHTML = translation;
        }
    });

    // Update HTML lang attribute
    document.documentElement.lang = currentLang === 'bn' ? 'bn' : 'en';

    // Update language button text
    const langButton = document.getElementById('current-lang');
    if (langButton) {
        langButton.textContent = currentLang === 'bn' ? 'বাংলা' : 'English';
    }
}

// Function to toggle language
function toggleLanguage() {
    currentLang = currentLang === 'bn' ? 'en' : 'bn';
    localStorage.setItem('language', currentLang);
    updateContent();
}

// Initialize on page load
document.addEventListener('DOMContentLoaded', () => {
    // Set initial content
    updateContent();

    // Add event listener to language toggle button
    const langToggle = document.getElementById('lang-toggle');
    if (langToggle) {
        langToggle.addEventListener('click', toggleLanguage);
    }

    // Smooth scroll for navigation links
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            const target = document.querySelector(this.getAttribute('href'));
            if (target) {
                target.scrollIntoView({
                    behavior: 'smooth',
                    block: 'start'
                });
            }
        });
    });
});
